void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) {
	if (htim == &htim4) {

		// RIGHT
		right_encoder.position = __HAL_TIM_GET_COUNTER(&htim1);
		if (!__HAL_TIM_IS_TIM_COUNTING_DOWN(&htim1)) {
			if (right_encoder.position < right_encoder.old_position) {
				right_encoder.old_position = right_encoder.position;
			}
		} else {
			if (right_encoder.position > right_encoder.old_position) {
				right_encoder.old_position = right_encoder.position;
			}
		}

		//LEFT
		left_encoder.position = __HAL_TIM_GET_COUNTER(&htim2);
		if (!__HAL_TIM_IS_TIM_COUNTING_DOWN(&htim2)) {
			if (left_encoder.position < left_encoder.old_position) {
				left_encoder.old_position = left_encoder.position;
			}
		} else {
			if (left_encoder.position > left_encoder.old_position) {
				left_encoder.old_position = left_encoder.position;
			}
		}

		//BACK
		back_encoder.position = __HAL_TIM_GET_COUNTER(&htim3);
		if (!__HAL_TIM_IS_TIM_COUNTING_DOWN(&htim3)) {
			if (back_encoder.position < back_encoder.old_position) {
				back_encoder.old_position = back_encoder.position;
			}
		} else {
			if (back_encoder.position > back_encoder.old_position) {
				back_encoder.old_position = back_encoder.position;
			}
		}

		//compute position
		odometry();

		//update
		right_encoder.old_position = right_encoder.position;
		left_encoder.old_position = left_encoder.position;
		back_encoder.old_position = back_encoder.position;

		process_data();
		//ODOMETRY DATA FOR MAIN MCU
		HAL_UART_Transmit_DMA(&huart3, (uint8_t *) tx_buffer, 25);
		//ODOMETRY DATA FOR BT MODULE
		HAL_UART_Transmit_DMA(&huart2, (uint8_t *) tx_buffer, 25);
		HAL_GPIO_TogglePin(GPIOC, GPIO_PIN_13);

		//ODOMETRY DATA FOR ESP32
	}
}

