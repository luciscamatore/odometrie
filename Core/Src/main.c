double rad2deg(double rad) {
	return (rad * (180.0 / M_PI)) - ((int) (rad * (180.0 / M_PI) / 360) * 360);
}

char msg[100];

//ODOMETRY STRUCT
typedef struct{
	int position, old_position;
} Odometer;

void Odometer_Init(Odometer *odo){
	odo->position = 0;
	odo->old_position = 0;
}

Odometer right_encoder, left_encoder, back_encoder;

//Odometry aux variables
double current_x = 0, current_y = 0, current_h = 0, current_h_rad = 0;
volatile int dn1 = 0, dn2 = 0, dn3 = 0;
double dx = 0, dy = 0, dtheta = 0, theta = 0;
void odometry() {
	dn1 = left_encoder.position - left_encoder.old_position;
	dn2 = right_encoder.position - right_encoder.old_position;
	dn3 = back_encoder.position - back_encoder.old_position;

	dtheta = CM_PER_TICK * ((dn2 - dn1) / L);
	dx = CM_PER_TICK * ((dn1 + dn2) / 2.0);
	dy = CM_PER_TICK * (dn3 + ((dn2 - dn1) / 2.0));


	theta += (dtheta / 2.0);
	current_x += dx * cos(theta) - dy * sin(theta);
	current_y += dx * sin(theta) + dy * cos(theta);
	theta += (dtheta / 2.0);
//	current_h_rad += dtheta;
	current_h = rad2deg(theta);
}

//DATA TRANSMISSION VARIABLES
char tx_buffer[MAX_BUFFER_SIZE];
int chars_written = 0;
void process_data() {
	chars_written = snprintf(tx_buffer, MAX_BUFFER_SIZE, "%+.2f,%+.2f,%+.2f\n",
			current_x, current_y, current_h);
	if (chars_written < 0 || chars_written > MAX_BUFFER_SIZE) {
		/*TODO: handle error*/
	} else {
		for (int i = chars_written; i < MAX_BUFFER_SIZE; i++) {
			tx_buffer[i] = '\n';
		}
	}
	tx_buffer[MAX_BUFFER_SIZE - 1] = '\0';
}

